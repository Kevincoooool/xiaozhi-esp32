static SemaphoreHandle_t lvgl_mux = NULL;

static lv_disp_drv_t disp_drv;
uint8_t *lcd_buffer= NULL;

static void st7305_lvgl_flush_cb(lv_disp_drv_t *drv, const lv_area_t *area, lv_color_t *color_map)
{
    esp_lcd_panel_handle_t panel_handle = (esp_lcd_panel_handle_t)drv->user_data;
    // int offsetx1 = area->x1;
    // int offsetx2 = area->x2;
    // int offsety1 = area->y1;
    // int offsety2 = area->y2;
    // // copy a buffer's content to a specific area of the display
    // esp_lcd_panel_draw_bitmap(panel_handle, offsetx1, offsety1, offsetx2 + 1, offsety2 + 1, color_map);
    uint32_t w = (area->x2 - area->x1 + 1);
    uint32_t h = (area->y2 - area->y1 + 1);
    // memset(lcd_buffer, 0, 384 * 21);
    // ESP_LOGI(TAG, "Flush area: %d, %d, %ld, %ld\n", area->x1, area->y1, w, h);
    for (int y = area->y1; y <= area->y2; y++)
    {
        for (int x = area->x1; x <= area->x2; x++)
        {
            // 获取LVGL的颜色值
            lv_color_t color = color_map[(y - area->y1) * w + (x - area->x1)];

            // 计算缓冲区位置和位偏移
            uint16_t byte_idx = (y >> 3) * 384 + x;
            if (byte_idx >= 384 * 21)
            {
                continue;
            }

            uint8_t bit_pos = y & 0x07;
            if (color.full)
            {
                lcd_buffer[byte_idx] |= (1 << bit_pos);
            }
            else
            {
                lcd_buffer[byte_idx] &= ~(1 << bit_pos);
            }
        }
    }
    esp_lcd_panel_draw_bitmap(panel_handle, 0, 0, w, h, lcd_buffer);

    lv_disp_flush_ready(&disp_drv);
}

static void st7305_increase_lvgl_tick(void *arg)
{
    /* Tell LVGL how many milliseconds has elapsed */
    lv_tick_inc(st7305_LVGL_TICK_PERIOD_MS);
}
static bool st7305_lvgl_lock(int timeout_ms)
{
    // Convert timeout in milliseconds to FreeRTOS ticks
    // If `timeout_ms` is set to -1, the program will block until the condition is met
    const TickType_t timeout_ticks = (timeout_ms == -1) ? portMAX_DELAY : pdMS_TO_TICKS(timeout_ms);
    return xSemaphoreTakeRecursive(lvgl_mux, timeout_ticks) == pdTRUE;
}

static void st7305_lvgl_unlock(void)
{
    xSemaphoreGiveRecursive(lvgl_mux);
}

static void st7305_lvgl_port_task(void *arg)
{
    ESP_LOGI(TAG, "Starting LVGL task");
    uint32_t task_delay_ms = st7305_LVGL_TASK_MAX_DELAY_MS;
    while (1)
    {
        // Lock the mutex due to the LVGL APIs are not thread-safe
        if (st7305_lvgl_lock(-1))
        {
            task_delay_ms = lv_timer_handler();
            // Release the mutex
            st7305_lvgl_unlock();
        }
        if (task_delay_ms > st7305_LVGL_TASK_MAX_DELAY_MS)
        {
            task_delay_ms = st7305_LVGL_TASK_MAX_DELAY_MS;
        }
        else if (task_delay_ms < st7305_LVGL_TASK_MIN_DELAY_MS)
        {
            task_delay_ms = st7305_LVGL_TASK_MIN_DELAY_MS;
        }
        vTaskDelay(pdMS_TO_TICKS(task_delay_ms));
    }
}


OledDisplay::OledDisplay(esp_lcd_panel_io_handle_t panel_io, esp_lcd_panel_handle_t panel,
    int width, int height, bool mirror_x, bool mirror_y, DisplayFonts fonts)
    : panel_io_(panel_io), panel_(panel), fonts_(fonts) {
    width_ = width;
    height_ = height;
    
    ESP_LOGI(TAG, "Initialize LVGL library");
    lv_init();
    // alloc draw buffers used by LVGL
    static lv_disp_draw_buf_t disp_buf; // contains internal graphic buffer(s) called draw buffer(s)
    // it's recommended to choose the size of the draw buffer(s) to be at least 1/10 screen sized
    lv_color_t *buf1 = (lv_color_t *)heap_caps_malloc(width_ * 20 * sizeof(lv_color_t), MALLOC_CAP_DMA);
    assert(buf1);
    // lv_color_t *buf2 = (lv_color_t *)heap_caps_malloc(width_ * 20 * sizeof(lv_color_t), MALLOC_CAP_DMA);
    // assert(buf2);
    // initialize LVGL draw buffers
    lv_disp_draw_buf_init(&disp_buf, buf1, NULL, width_ * 20);
    lcd_buffer = (uint8_t *)heap_caps_malloc(384 * 21, MALLOC_CAP_SPIRAM);
    ESP_LOGI(TAG, "Register display driver to LVGL");
    lv_disp_drv_init(&disp_drv);
    disp_drv.hor_res = width_;
    disp_drv.ver_res = height_;
    disp_drv.flush_cb = st7305_lvgl_flush_cb;
    disp_drv.draw_buf = &disp_buf;
    disp_drv.user_data = panel_;
    disp_drv.sw_rotate = 1;
    // 设置旋转方向(0/90/180/270度)
    disp_drv.rotated = LV_DISP_ROT_90; // 可以改为LV_DISP_ROT_NONE/LV_DISP_ROT_90/LV_DISP_ROT_180/LV_DISP_ROT_270

    lv_disp_t *disp = lv_disp_drv_register(&disp_drv);

    ESP_LOGI(TAG, "Install LVGL tick timer");
    // Tick interface for LVGL (using esp_timer to generate 2ms periodic event)
    const esp_timer_create_args_t lvgl_tick_timer_args = {
        .callback = &st7305_increase_lvgl_tick,
        .name = "lvgl_tick"};
    esp_timer_handle_t lvgl_tick_timer = NULL;
    ESP_ERROR_CHECK(esp_timer_create(&lvgl_tick_timer_args, &lvgl_tick_timer));
    ESP_ERROR_CHECK(esp_timer_start_periodic(lvgl_tick_timer, st7305_LVGL_TICK_PERIOD_MS * 1000));

    lvgl_mux = xSemaphoreCreateRecursiveMutex();
    assert(lvgl_mux);
    ESP_LOGI(TAG, "Create LVGL task");
    xTaskCreate(st7305_lvgl_port_task, "LVGL", st7305_LVGL_TASK_STACK_SIZE, NULL, st7305_LVGL_TASK_PRIORITY, NULL);

    // SetupUI_128x64();
    SetupUI();
}
OledDisplay::~OledDisplay() {
    if (content_ != nullptr) {
        lv_obj_del(content_);
    }
    if (status_bar_ != nullptr) {
        lv_obj_del(status_bar_);
    }
    if (side_bar_ != nullptr) {
        lv_obj_del(side_bar_);
    }
    if (container_ != nullptr) {
        lv_obj_del(container_);
    }

    if (panel_ != nullptr) {
        esp_lcd_panel_del(panel_);
    }
    if (panel_io_ != nullptr) {
        esp_lcd_panel_io_del(panel_io_);
    }
    // lvgl_port_deinit();
}

bool OledDisplay::Lock(int timeout_ms) {
    return st7305_lvgl_lock(timeout_ms);
}

void OledDisplay::Unlock() {
    st7305_lvgl_unlock();
}

void OledDisplay::SetChatMessage(const char* role, const char* content) {
    DisplayLockGuard lock(this);
    if (chat_message_label_ == nullptr) {
        return;
    }

    // Replace all newlines with spaces
    std::string content_str = content;
    std::replace(content_str.begin(), content_str.end(), '\n', ' ');

    if (content_right_ == nullptr) {
        lv_label_set_text(chat_message_label_, content_str.c_str());
    } else {
        if (content == nullptr || content[0] == '\0') {
            lv_obj_add_flag(content_right_, LV_OBJ_FLAG_HIDDEN);
        } else {
            lv_label_set_text(chat_message_label_, content_str.c_str());
            lv_obj_clear_flag(content_right_, LV_OBJ_FLAG_HIDDEN);
        }
    }
}

